        // After cumulative sum from end: [1, 2+1=3, 3+2=5, 4+3=7] -> [1, 3, 5, 7]
        // But wait, it's from end to start: [1+2, 2+3, 3+4, 4] = [3, 5, 7, 4]
        // Actually: for i in (1..N).rev() means i = 3,2,1
        // coeff[3] += coeff[2] -> 4+3=7
