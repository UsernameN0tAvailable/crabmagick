//! General-purpose Huffman tables for streaming/single-pass encoding.
//!
//! Trained from 395 diverse images across 7 image sets
//! (CID22, CLIC2025, Kodak, gb82, kadid10k, etc.).
//!
//! Three families: ycbcr_444, ycbcr_420 (also used for 422), xyb.
//! Each family has tables for 30 quality tiers (Q0-Q100).
//!
//! Typical overhead vs per-image optimal: ~2.5%.
//! Improvement over JPEG Annex K standard tables: 5-12pp.

use crate::jpeg_encode::huffman::optimize::{HuffmanTableSet, OptimizedTable};

pub(crate) const QUALITY_TIERS: &[u8] = &[
    0, 5, 10, 15, 20, 25, 30, 35, 40, 45, 50, 55, 60, 65, 70, 75, 80, 85, 89, 90, 91, 92, 93, 94,
    95, 96, 97, 98, 99, 100,
];

/// Returns the index into QUALITY_TIERS for the nearest tier <= quality.
/// If quality < 0, returns index 0. If quality > 100, returns last index.
pub(crate) fn nearest_quality_tier(quality: f32) -> usize {
    let q = quality.round() as i32;
    match QUALITY_TIERS.binary_search(&(q.clamp(0, 100) as u8)) {
        Ok(idx) => idx,
        Err(idx) => idx.saturating_sub(1),
    }
}

