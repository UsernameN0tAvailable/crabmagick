/// Select general-purpose Huffman tables for the given quality and encoding mode.
///
/// Converts the quality to internal 0-100 scale, finds the nearest quality tier,
/// and returns the appropriate table family:
/// - XYB: shared DC+AC tables for all 3 components
/// - YCbCr 4:4:4: separate luma/chroma tables, full resolution
/// - YCbCr 4:2:0/4:2:2/4:4:0: separate luma/chroma tables, subsampled
///
/// Works with all [`Quality`] variants (jpegli scale, mozjpeg, SSIM2, Butteraugli distance).
pub(crate) fn select_tables(
    quality: &crate::jpeg_encode::encode::encoder_types::Quality,
    use_xyb: bool,
    subsampling: crate::jpeg_encode::types::Subsampling,
) -> HuffmanTableSet {
    let tier_idx = nearest_quality_tier(quality.to_internal());
    if use_xyb {
        xyb_tables(tier_idx)
    } else {
        match subsampling {
            crate::jpeg_encode::types::Subsampling::S444 => ycbcr_444_tables(tier_idx),
            _ => ycbcr_420_tables(tier_idx),
        }
    }
}
